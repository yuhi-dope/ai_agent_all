/**
 * 画像を生成する（アイキャッチ + Mermaid図解）
 * @param {Object} ネタ
 * @returns {Object} { アイキャッチ, 図解 }
 */
function 画像を生成(ネタ) {
  let アイキャッチ = null;
  let 図解 = null;

  // アイキャッチ画像（エラー時はスキップ）
  try {
    const アイキャッチプロンプト = `Create a modern, clean, abstract visual for a blog article about: ${ネタ.タイトル}. Style: Professional, minimalist, tech-inspired. Colors: Blue and white tones. Do NOT include any text in the image.`;
    アイキャッチ = Geminiで画像生成(アイキャッチプロンプト);
  } catch (e) {
    Logger.log('アイキャッチ生成スキップ: ' + e.message);
  }

  // Mermaid図解（エラー時はスキップ）
  try {
    const mermaidコード = Mermaidコードを生成(ネタ.タイトル);
    図解 = mermaidコード ? Mermaidを画像に変換(mermaidコード) : null;
  } catch (e) {
    Logger.log('Mermaid図解スキップ: ' + e.message);
  }

  // Google Driveに保存
  try {
    if (アイキャッチ) {
      const フォルダ = 画像フォルダを取得();
      フォルダ.createFile(アイキャッチ).setName(`アイキャッチ_${ネタ.行番号}.png`);
    }
    if (図解) {
      const フォルダ = 画像フォルダを取得();
      フォルダ.createFile(図解).setName(`図解_${ネタ.行番号}.png`);
    }
  } catch (e) {
    Logger.log('画像保存スキップ: ' + e.message);
  }

  return { アイキャッチ, 図解 };
}

/** 画像保存用フォルダを取得（なければ作成） */
function 画像フォルダを取得() {
  const フォルダ名 = 'shachotwo-SNSAI-画像';
  const フォルダ一覧 = DriveApp.getFoldersByName(フォルダ名);
  if (フォルダ一覧.hasNext()) return フォルダ一覧.next();
  return DriveApp.createFolder(フォルダ名);
}
