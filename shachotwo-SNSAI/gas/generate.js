/**
 * 3媒体コンテンツ一括生成
 * 1つのネタから X短文 / X記事ダイジェスト / noteフル / LinkedIn の4本を生成
 */

/**
 * 全媒体の下書きを一括生成する
 * @param {Object} ネタ - { タイトル, 本文, 業種, タグ, 行番号 }
 * @param {Array} 関連知見 - 頭のDBから取得した関連エントリ
 * @returns {Object} { X短文, X記事, note, LinkedIn }
 */
function 全媒体の下書きを生成(ネタ, 関連知見) {
  const 設定 = 設定を取得();
  const 知見テキスト = 関連知見.map(e => `[${e.カテゴリ}] ${e.テキスト}`).join('\n');

  // 1. noteフル版を最初に生成（最も情報量が多い）
  const note内容 = note記事を生成(ネタ, 知見テキスト, 設定);

  // 2. X短文
  const X短文 = X短文投稿を生成(ネタ, 知見テキスト, 設定);

  // 3. X記事ダイジェスト（noteの前半50%をベースに）
  const X記事 = X記事ダイジェストを生成(note内容, 設定);

  // 4. LinkedIn
  const LinkedIn = LinkedIn投稿を生成(ネタ, 知見テキスト, 設定);

  return { X短文, X記事, note: note内容, LinkedIn };
}

